import EasyTyper from "easy-typer-js";
export default {
  created() {
    this.init();
    this.timer = setInterval(this.runTime, 1000);
    this.getBlogInfo();
  },
  data: function() {
    return {
      tip: false,
      time: "",
      obj: {
        output: "休对故人思故国，且将新火试新茶。诗酒趁年华。",
        isEnd: false,
        speed: 300,
        singleBack: false,
        sleep: 0,
        type: "rollback",
        backSpeed: 40,
        sentencePause: true
      },
      articleList: [],
      blogInfo: {
        about: '',
        websiteAvatar: '',
        motto: '',
        websiteName: '',
        websiteNotice: '',
        viewsCount: 0
      },
      current: 1,
      total: 5,
      totalPage: 1,
      tagList: [],
      tagCount: 0,
    };
  },
  methods: {
    // 初始化
    init() {
      document.title = this.$route.meta.title;
      // 一言Api进行打字机循环输出效果
      fetch("https://v1.hitokoto.cn?c=i")
        .then(res => {
          return res.json();
        })
        .then(({ hitokoto }) => {
          this.initTyped(hitokoto);
        });
    },
    initTyped(input, fn, hooks) {
      const obj = this.obj;
      // eslint-disable-next-line no-unused-vars
      const typed = new EasyTyper(obj, input, fn, hooks);
    },
    scrollDown() {
      window.scrollTo({
        behavior: "smooth",
        top: document.documentElement.clientHeight
      });
    },
    runTime() {
      var timeold = new Date().getTime() - new Date("October 6,2021").getTime();
      var msPerDay = 24 * 60 * 60 * 1000;
      var daysold = Math.floor(timeold / msPerDay);
      var str = "";
      var day = new Date();
      str += daysold + "天";
      str += day.getHours() + "时";
      str += day.getMinutes() + "分";
      str += day.getSeconds() + "秒";
      this.time = str;
    },
    getBlogInfo() {
      this.blogInfo = this.$$store.state.blogInfo;
      // this.axios.get("/bloginfo").then(({ data }) => {
      //   this.blogInfo = data.data[0];
      //   this.$store.commit("checkBlogInfo", data.data[0]);
      // });
      this.infiniteHandler();
      this.loadTags();
    },
    // infiniteHandler($state) {
    infiniteHandler() {
      let that = this;  
      let md = require("markdown-it")();
      this.axios.get("/article/articles", {
          params: {
            currentPage: this.current
          }
        })
        .then(({ data }) => {
          that.total = data.data.total;
          that.totalPage = data.data.pages;
          // 去除markdown标签
          data.data.records.forEach(item => {
            item.content = md
              .render(item.content)
              .replace(/<\/?[^>]*>/g, "")
              .replace(/[|]*\n/, "")
              .replace(/&npsp;/gi, "");
          });
          that.articleList = data.data.records;
          that.current = data.data.current;
          // that.$state.loaded();
        }
      );
    },
    loadTags(){
      var that = this;  
      this.axios.get("/tag/tags").then(({ data }) => {
            // console.log(data);
            if(data.code==200){
              that.tagList = data.data;
              that.tagCount = that.tagList.length;
              that.$store.state.tagList = data.data;
            }
        });
    },
  },
  watch : {
    current(){
      this.infiniteHandler();
    }
  },
  computed: {
    isRight() {
      return function(index) {
        if (index % 2 == 0) {
          return "article-cover left-radius";
        }
        return "article-cover right-radius";
      };
    }
  }
};
</script>

